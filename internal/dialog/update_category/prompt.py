    def _format_list(self, items: list[str] | list[dict]) -> str:
        """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —Å–ø–∏—Å–æ–∫ –≤ —á–∏—Ç–∞–µ–º—ã–π –≤–∏–¥"""
